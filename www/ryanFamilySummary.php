		<li>$1500 for a GPA of 3.00-3.49/4.00 (maximum GPA of 4.00)</li>
		<li>$2000 for a GPA of 3.5-3.74/4.00</li>
		<li>$2500 for a GPA of 3.75+/4.00</li>
	</ul>
