<h1>Our Heritage </h1>
      <p> Delta Tau Delta was founded in 1858 at Bethany College in present day West Virginia. Eight men, angered by a fixed vote for a prize in oratory to be given at the Neotrophian Literary Society (a forum for students to practice and demonstrate skills in poetry, public speaking and writing essays), responded by forming a secret society. The purpose of the new society, known only by the Greek letters Delta Tau Delta, was to see the Neotrophian Society returned to popular control from the Phi Kappa Psi Fraternity, and to form an organization of the student body drawn together by common aims, brotherly regard and desire for mutual support.<br />
      	</p>
      <p>Delta Tau Delta was born of the knowledge that integrity is essential. The founding principles of Truth, Courage, Faith and Power have provided a guide for both the Fraternity and its membership.<br />
      	</p>
      <p>Since 1858 the Fraternity has spread to nearly 200 campuses with over 120 active chapters and colonies comprised of about 6,500 undergraduate members. For over 150 years, Delta Tau Delta has in excess of 150,000 men who have become members of the Fraternity.    </p>
