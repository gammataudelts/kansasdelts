<h1>About Gamma Tau</h1>
    <p>Delta Tau Delta is recognized under the Interfraternity Council at the University of Kansas as a recent colonization. The Kansas Delts are currently comprised of close to 50 members striving for academic achievement with a fun social environment. The well rounded Delta Tau Delta man encompasses the equal values of truth, faith, power, and courage.</p>
    <p>Academically, the University of Kansas Delta Tau Delta's ask members to have a minimum GPA of 2.75. Pledge groups are created for study hours to increase flexibility in schedules. Failure to maintain such grades can result in suspension from social activities, and removal from the chapter.</p>
    <p>Delts take intramurals to a competitive level. All Delta Tau Delta members are encouraged to get in the games. Recent intramural success has been seen in sports such as soccer and dodgeball. The Kansas Delts hope to use the varying skills of new members to continuously increase in the IFC Hill Ranking.</p>
    <p>There are numerous leadership and scholarship opportunities available through the Delta Tau Delta organization. Two of the most notable scholarships include The Kershner Scholarship, and the 'Committed to Lives of Excellence' Scholarship, both worth up to $1,000. We also offer opportunities to attend UIFI and Leadershape, leadership programs built on strengthening your values and abilities to lead. Scholorships are given through the UIFI program.</p>
    <p>These next few years are rebuilding stages for the Kansa Delts. With every new member comes a stronger brotherhood and an opportunity to live a life of excellence. For more information, please visit the national website, <a href="delts.org">www.delts.org</a>.</p>
